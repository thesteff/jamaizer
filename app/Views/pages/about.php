<h1>A propos</h1>
<div class="row">
    <h4 class="panel-heading">Le Grenoble Reggae Orchestra</h4>
</div>

<div class="row panel-body">
    <div id="cadre" class="col-lg-12">

        <p><b>est un collectif de musiciens œuvrant pour la redécouverte et le partage de la musique reggae via
                l’organisation de scènes ouvertes.</b></p>

        <p><b>Le GRO rassemble plus d'une centaine de musiciens amoureux du reggae.</b> Certains sont professionnels,
            d’autres sont amateurs, mais tous sont actifs dans les musiques actuelles (jazz, rock, reggae, gospel, ...).
            La plupart d’entre eux font partie de l’un d’un des innombrables groupes de la région grenobloise, notamment
            connue pour son attachement au reggae.</p>

        <p>Réunis sous la houlette du GRO, association loi 1901, ces musiciens passionnés ont pour objectif <b>rendre
                hommage à cette musique</b> extrêmement populaire en explorant son répertoire (Bob Marley, Gladiators,
            Steel Pulse, Aswad…), <b>en l’interprétant au format XL </b>(plus de 10 musiciens sur scène) <b>et en
                ouvrant la scène aux volontaires !</b></p>

        <p>Afin de partager le plaisir jubilatoire d’interpréter cette musique, ils invitent tous les musiciens
            amateurs, quel que soit leur niveau, à monter sur scène à leurs côtés (ou à leur place).</p>
        <p>Depuis une <b>première jam mythique organisée en 2011</b> à l'occasion des 30 ans de la disparition de Bob
            Marley, le GRO s'est développé en enrichissant son répertoire (plus de 130 références</b> jouées) ainsi
            qu'en diversifiant son action (encadrement de <b>stages de préparation</b> depuis 2014).</p>
        <p><b>Sur scène, le GRO accueille les instruments suivants :</b><br>
            basse / batterie / clavier / guitare / percussions / chant (lead & choeur) / soufflants.</p>

        <p>Le projet est né de l’initiative de <a href="http://s.plotto.free.fr/" target="_blanck">Stéphane Plotto.</a>
        </p>
    </div>
</div>